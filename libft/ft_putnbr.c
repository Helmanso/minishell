#include "libft.h"

void	ft_putnbr(int n)
{
	long long i;

	i = (long long)n;
	if (i < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	if (i <= 9)
		ft_putchar(i + '0');
	else
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
